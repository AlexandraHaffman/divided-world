import hashlib

def _rng(*parts):
    """Детерминированный дробный шум в [-1,1] из строкового ключа."""
    h = hashlib.sha256("|".join(str(p) for p in parts).encode()).hexdigest()
    v = int(h[:8], 16) / 0xFFFFFFFF      # 0..1
    return v * 2 - 1

def clamp(x, lo=1.0, hi=10.0):
    return max(lo, min(hi, x))

